                       <b>For Academia-</b><br>
 IAC aims to ensure that the academia keeps updating itself with the changing industrial paradigms, and encourage the students to cultivate a more pragmatic outlook regarding the Industry and its expectations. Considering the versatile requirements of the Industry, IAC focuses on helping the academia stay abreast with the latest industrial developments, and learn about avenues where there is scope for collaboration.<br><br> 
<b>For Industry-</b><br>
The IAC plays a vital role as a forum for such Industry-Academia interaction, and also provides an opportunity for the Industry to present areas where they would seek research assistance from the Academia. The industry could also benefit from the knowledge of the on-going R&D projects, and could then actively participate in those that might interest them.  <br>
The exchange of perspectives between the industry personnel and the academia can provide an impetus for various research and development opportunities; it could later lead to efficient and holistic industrial processes. 

